#include<iostream>
using namespace std;

int main() {
	for (int i = 5; i <= 50; i+=3)
		cout << i << " ";
	cout << endl;
	for (int i = 80; i >= 10; i-=2)
		cout << i << " ";
	cout << endl;
	for (int i = 2; i <= 200; i*=5)
		cout << i << " ";
	cout << "\n*Ascends to the next dimension.*\n";
}